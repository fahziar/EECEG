    private void btnPrivateAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPrivateAMouseClicked
        fc.setAcceptAllFileFilterUsed(false);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Private key (.pri)", "pri");
        fc.addChoosableFileFilter(filter);
        fc.showOpenDialog(this);
        
        flPrivateA = fc.getSelectedFile();
        lblPrivateA.setText(flPrivateA.getName());
        
        try 
        {
           privateKey = Long.parseLong(FileUtils.readLines(fc.getSelectedFile()).get(0));
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, "Cannot open file");
        }
        
        fc.setAcceptAllFileFilterUsed(true);
        fc.removeChoosableFileFilter(filter);
    }//GEN-LAST:event_btnPrivateAMouseClicked

    private void btnLoadPlainMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoadPlainMouseClicked
        fc.showOpenDialog(this);
        
        File f = fc.getSelectedFile();
        
        try {
            plainText = Files.readAllBytes(f.toPath());
            txtPlain.setText(new String(plainText, "UTF-8"));
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, "Cannot open file");
        }
    }//GEN-LAST:event_btnLoadPlainMouseClicked

