        fc.addChoosableFileFilter(filter);
        fc.showOpenDialog(this);
        
        flPublicA = fc.getSelectedFile();
        lblPublicA.setText(flPublicA.getName());
        
        //TODO: Load public key dari file
        
        fc.setAcceptAllFileFilterUsed(true);
