    private void btnGenerateKeysMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGenerateKeysMouseClicked
        //Generate key
        privateKey = new BigInteger (63, new Random()).longValueExact();
        
        fc.setAcceptAllFileFilterUsed(false);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Private key (.pri)", "pri");
        fc.addChoosableFileFilter(filter);
        fc.showSaveDialog(this);
        
        //Save private key
        File out = fc.getSelectedFile();
        if (out.getName().lastIndexOf(".") == -1)
        {
            out = new File(out.getParentFile(), out.getName() + ".pri");
        } else if (out.getName().substring(out.getName().lastIndexOf("."),out.getName().length()).equalsIgnoreCase("pri"))
        {
            out = new File(out.getParentFile(), out.getName() + ".pri");
        }
        
        try {
            FileUtils.writeStringToFile(out, String.valueOf(privateKey), "UTF-8");
        } catch (Exception e)
        {
            
        }
        
        //Save public key
        fc.removeChoosableFileFilter(filter);
        filter = new FileNameExtensionFilter("Public key (.pub)", "pub");
        fc.addChoosableFileFilter(filter);
        fc.showSaveDialog(this);
        
        if (out.getName().lastIndexOf(".") == -1)
        {
            out = new File(out.getParentFile(), out.getName() + ".pub");
        } else if (out.getName().substring(out.getName().lastIndexOf("."),out.getName().length()).equalsIgnoreCase("pri"))
        {
            out = new File(out.getParentFile(), out.getName() + ".pub");
        }
        
        try {
            //TODO: Save public key
        } catch (Exception e)
        {
            
        }
        
        fc.removeChoosableFileFilter(filter);
        fc.setAcceptAllFileFilterUsed(true);
    }//GEN-LAST:event_btnGenerateKeysMouseClicked

